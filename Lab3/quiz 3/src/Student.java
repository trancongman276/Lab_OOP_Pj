
public class Student {
	private String name,ID,Group;
	private int Score;
	public Student() {
		
	}
	
	public int getScore() {
		return Score;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setID(String iD) {
		ID = iD;
	}
	
	public void setGroup(String Group) {
		this.Group = Group;
	}

	public void setScore(int score) {
		Score = score;
	}
	
}
